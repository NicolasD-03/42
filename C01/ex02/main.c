#include <stdio.h>

void	ft_swap(int *a, int *b);

int	main(void)
{
	int	a;
	int	b;

	a = 10;
	b = 5;
	printf("Base : a = %d, b = %d", a, b);
	ft_swap(&a, &b);
	printf("\nSwaped : a = %d, b = %d", a, b);
	return (0);
}
